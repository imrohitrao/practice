# List
my_list = [1, 2, 3]
print("List:")
for item in my_list:
    print(item)

# Tuple
my_tuple = (4, 5, 6)
print("\nTuple:")
for item in my_tuple:
    print(item)

# Dictionary
my_dict = {'a': 1, 'b': 2, 'c': 3}

# Iterate through keys
for key in my_dict:
    print(key)

# Iterate through values
for value in my_dict.values():
    print(value)

# Iterate through key-value pairs
for key, value in my_dict.items():
    print(f"{key}: {value}")

# Set
my_set = {10, 11, 12}
print("\nSet:")
for item in my_set:
    print(item)